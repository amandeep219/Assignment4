
public class Lecturer {
	
	String name;
	int age;
	Department dept;
	int salary;
	
	
	
	public Lecturer() {
		
	}
	public Lecturer(String name, int age, Department dept, int salary) {
		super();
		this.name = name;
		this.age = age;
		this.dept = dept;
		this.salary = salary;
	}

}
